def parcel_ring(geometry):
    """GeoJSON リング（[lng,lat]・末尾は始点の繰り返し）→ [[lat,lng]]"""
    return [[lat, lng] for lng, lat in geometry["coordinates"][0][:-1]]


def polygon_area_tsubo(latlngs):
    """[[lat,lng]] ポリゴンの面積を坪で返す（重心緯度での平面近似 + 靴ひも公式）。"""
    if not latlngs or len(latlngs) < 3:
        return 0
    lat0 = sum(p[0] for p in latlngs) / len(latlngs)
    lng0 = latlngs[0][1]
    m_per_lat = 111320
    m_per_lng = 111320 * math.cos(math.radians(lat0))
    pts = [((lng - lng0) * m_per_lng, (lat - lat0) * m_per_lat) for lat, lng in latlngs]
    area2 = 0.0
    for i in range(len(pts)):
        x1, y1 = pts[i]
        x2, y2 = pts[(i + 1) % len(pts)]
        area2 += x1 * y2 - x2 * y1
    sqm = abs(area2) / 2
    return round(sqm / 3.305785 * 100) / 100


def convex_hull(points):
    """点群の凸包（Andrew's monotone chain）。サンプル案件の領域自動生成に使う。"""
    pts = sorted(points)
    if len(pts) < 3:
        return pts

    def cross(o, a, b):
        return (a[0] - o[0]) * (b[1] - o[1]) - (a[1] - o[1]) * (b[0] - o[0])

    lower = []
    for p in pts:
        while len(lower) >= 2 and cross(lower[-2], lower[-1], p) <= 0:
            lower.pop()
        lower.append(p)
    upper = []
    for p in reversed(pts):
        while len(upper) >= 2 and cross(upper[-2], upper[-1], p) <= 0:
            upper.pop()
        upper.append(p)
    return lower[:-1] + upper[:-1]


