def _num(v):
    """numeric → JSON 数値。整数値は int にして「500.0%」のような表示を避ける。"""
    if v is None:
        return None
    f = float(v)
    return int(f) if f.is_integer() else f


def _iso(dt):
    return dt.isoformat() if dt is not None else None


def _visit_json(row):
    return {
        "id": row["id"],
        "user": row["user_name"],
        "comment": row["comment"],
        "date": _iso(row["date"]),
        "directOrTel": row["direct_or_tel"],
        "meetingType": row["meeting_type"],
        "nextDate": _iso(row["next_date"]) or "",
        "progress": row["progress"],
        "principal": row["principal"],
    }


def _land_json(row, visits=None):
    """lands × parcels × chibankuiki の JOIN 行 → Land JSON。
    aza/chiban/polygon は筆マスタ・地番区域マスタから導出。"""
    land = {
        "id": row["id"],
        "parcelId": row["parcel_id"],
        "aza": row["aza"],
        "chiban": row["chiban"],
        "owners": row["owners"],
        "description": row["description"],
        "areaTsubo": _num(row["area_tsubo"]),
        "status": row["status"],
        "createdAt": _iso(row["created_at"]),
        "updatedAt": _iso(row["updated_at"]),
        "polygon": parcel_ring(row["geometry"]),
    }
    if visits is not None:
        land["visits"] = visits
    return land


def _project_json(row, lands=None):
    proj = {
        "id": str(row["id"]),
        "name": row["name"],
        "description": row["description"],
        "createdAt": _iso(row["created_at"]),
        "updatedAt": _iso(row["updated_at"]),
        "polygon": row["polygon"],
        "address": row["address"],
        "access": row["access"],
        "currentFar": _num(row["current_far"]),
        "targetFar": _num(row["target_far"]),
        "frontRoads": row["front_roads"],
    }
    if lands is not None:
        proj["lands"] = lands
    return proj


_LAND_SELECT = """
    SELECT l.*, c.name AS aza, p.chiban, p.geometry,
           COALESCE((
             SELECT jsonb_agg(jsonb_build_object(
                      'name', o.name,
                      'share', CASE WHEN o.share_num IS NOT NULL
                                    THEN o.share_num || '/' || o.share_den ELSE '' END
                    ) ORDER BY o.id)
               FROM land_owners o WHERE o.land_id = l.id
           ), '[]'::jsonb) AS owners
      FROM lands l
      JOIN parcels p ON p.id = l.parcel_id
      JOIN chibankuiki c ON c.id = p.chibankuiki_id
"""


def _parse_project_id(project_id):
    try:
        return int(project_id)
    except (TypeError, ValueError):
        raise ApiError(404, "案件が見つかりません")


def _parse_parcel_id(parcel_id):
    """API から来た parcelId（文字列のことがある）を int に正規化する。"""
    try:
        return int(parcel_id)
    except (TypeError, ValueError):
        raise ApiError(400, "筆マスタに存在しない筆です")


def _parse_dt(value, fallback=None):
    """ISO 文字列（'Z' 終端含む）→ datetime。空・不正は fallback。"""
    if not value:
        return fallback
    try:
        return datetime.fromisoformat(str(value).replace("Z", "+00:00"))
    except ValueError:
        return fallback


