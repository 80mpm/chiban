"""
共有データレイヤー（PostgreSQL 版）

旧 data.js が localStorage 上に持っていた案件・土地・訪問記録を PostgreSQL に
正規化テーブル（projects / lands / visits）で永続化する。筆マスタも parcels
テーブルに取り込み、土地の parcelId に外部キー制約を張る。
住所階層は住所マスタ 3 テーブル（prefectures / shikuchoson / chibankuiki）で持ち、
筆は地番区域（大字町丁目）への参照 (chibankuiki_id) で所在を表す。

設計上の不変条件（旧 data.js から引き継ぎ）:
    - 土地は筆マスタへの参照 (parcel_id) でのみ作成・変更できる
    - 町名 (aza)・地番 (chiban)・領域 (polygon) は読み出しのたびに
      parcels・chibankuiki から再導出する（lands には保存しない）
    - 1 案件内で同じ筆は重複できない（UNIQUE (project_id, parcel_id)）
    - 訪問記録は追加のみ（履歴のため編集・削除 API を持たない）
    - ID は意味を持たないサロゲートキー。JIS コード・町名・地番などの
      業務属性はただのカラム + UNIQUE 制約で表現する（ID から業務情報を読み取らない）

接続先は環境変数 DATABASE_URL（既定: docker-compose.yml の postgres:17）。
"""

