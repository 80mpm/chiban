def get_parcel_towns(conn):
    """町名（地番区域）の一覧と筆数。土地の追加モード等のプルダウン用。"""
    rows = conn.execute(
        """SELECT c.name, count(*) AS n
             FROM parcels p JOIN chibankuiki c ON c.id = p.chibankuiki_id
            GROUP BY c.id, c.name ORDER BY c.name"""
    ).fetchall()
    return [{"name": r["name"], "count": r["n"]} for r in rows]


def get_parcels_by_town(conn, town, with_geometry=False):
    """指定町名の筆一覧。プルダウンの選択肢用は属性のみ（parcelId と地番）。
    with_geometry=True で領域 [[lat,lng]] 付き（土地の追加モードの候補筆表示用）。"""
    cols = "p.id, p.chiban" + (", p.geometry" if with_geometry else "")
    rows = conn.execute(
        f"""SELECT {cols}
             FROM parcels p JOIN chibankuiki c ON c.id = p.chibankuiki_id
            WHERE c.name = %s ORDER BY p.id""",
        (town,),
    ).fetchall()
    if with_geometry:
        return [
            {"parcelId": r["id"], "chiban": r["chiban"], "polygon": parcel_ring(r["geometry"])}
            for r in rows
        ]
    return [{"parcelId": r["id"], "chiban": r["chiban"]} for r in rows]


def get_parcels(conn):
    """筆マスタ全件。旧 KOUZU_XML_DATA.geojson と同じ features 形で返す（デバッグ用）。"""
    rows = conn.execute(
        """SELECT p.*, c.name AS town_name
             FROM parcels p JOIN chibankuiki c ON c.id = p.chibankuiki_id
            ORDER BY p.id"""
    ).fetchall()
    features = [
        {
            "type": "Feature",
            "properties": {
                "parcelId": r["id"],
                "chibankuikiId": r["chibankuiki_id"],
                "chibankuikiName": r["town_name"],
                "chiban": r["chiban"],
            },
            "geometry": r["geometry"],
        }
        for r in rows
    ]
    return {"features": features}


def get_projects_tree(conn):
    """全案件を lands・visits 込みのツリーで返す（3 クエリで組み立て）。"""
    proj_rows = conn.execute("SELECT * FROM projects ORDER BY id").fetchall()
    land_rows = conn.execute(_LAND_SELECT + " ORDER BY l.project_id, l.created_at, l.id").fetchall()
    visit_rows = conn.execute("SELECT * FROM visits ORDER BY land_id, date, id").fetchall()

    visits_by_land = {}
    for v in visit_rows:
        visits_by_land.setdefault(v["land_id"], []).append(_visit_json(v))

    lands_by_proj = {}
    for l in land_rows:
        lands_by_proj.setdefault(l["project_id"], []).append(
            _land_json(l, visits_by_land.get(l["id"], []))
        )

    return [_project_json(p, lands_by_proj.get(p["id"], [])) for p in proj_rows]


