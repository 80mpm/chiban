def _load_kouzu_features():
    """kouzu_xml_data.js から GeoJSON features を読み出す。
    `window.KOUZU_XML_DATA = {...};` 形式の自動生成ファイルなので、
    代入の右辺だけを JSON として取り出す。"""
    with open(KOUZU_JS_PATH, encoding="utf-8") as f:
        src = f.read()
    m = re.search(r"window\.KOUZU_XML_DATA\s*=\s*(\{.*\});?\s*$", src, re.S)
    if not m:
        raise RuntimeError("kouzu_xml_data.js から KOUZU_XML_DATA を抽出できません")
    data = json.loads(m.group(1))
    return data["geojson"]["features"]


def _load_seed_parcels():
    """筆マスタのシード行を返す。
    台東区全図面シード（kouzu_parcels_seed.json.gz）があればそれを、
    なければ kouzu_xml_data.js（5図面・595筆）を使う。
    ID はシードに含めない（投入時に parcels.id へサロゲートキーが振られる）。"""
    if os.path.exists(PARCEL_SEED_PATH):
        with gzip.open(PARCEL_SEED_PATH, "rt", encoding="utf-8") as f:
            data = json.load(f)
        return data["parcels"], os.path.basename(PARCEL_SEED_PATH)
    rows = [
        {
            "oaza": f["properties"].get("oaza", ""),
            "chome": f["properties"].get("chome", "") or "",
            "chiban": f["properties"]["chiban"],
            "geometry": f["geometry"],
        }
        for f in _load_kouzu_features()
    ]
    return rows, os.path.basename(KOUZU_JS_PATH)


def _kanji_number(n):
    """1〜99 の整数を漢数字にする（位置参照情報の丁目表記に合わせる用）。"""
    ones = "一二三四五六七八九"
    if not 1 <= n <= 99:
        raise ValueError(f"漢数字に変換できない丁目です: {n}")
    if n < 10:
        return ones[n - 1]
    tens, rem = divmod(n, 10)
    return (ones[tens - 1] if tens > 1 else "") + "十" + (ones[rem - 1] if rem else "")


def _seed_town_name(oaza, chome):
    """筆シードの (大字, 丁目) → 位置参照情報の大字町丁目名。
    シード側の丁目は算用数字（例 '３丁目'）、住所マスタ側は漢数字（例 '上野三丁目'）
    のため、突合時にここで表記を合わせる。"""
    if not chome:
        return oaza
    m = re.fullmatch(r"([0-9０-９]+)丁目", chome)
    if not m:
        raise RuntimeError(f"丁目の表記を解釈できません: {oaza}{chome}")
    n = int("".join(chr(ord(c) - 0xFEE0) if c >= "０" else c for c in m.group(1)))
    return oaza + _kanji_number(n) + "丁目"


def seed_address_master_if_empty(conn):
    """住所マスタ（prefectures / shikuchoson / chibankuiki）を位置参照情報 CSV から投入する。"""
    n = conn.execute("SELECT count(*) AS n FROM chibankuiki").fetchone()["n"]
    if n > 0:
        return False
    with open(ADDRESS_CSV_PATH, encoding="shift_jis", newline="") as f:
        rows = list(csv.DictReader(f))
    pref_ids = {}  # 都道府県 jis_code → prefectures.id
    muni_ids = {}  # 市区町村 jis_code → shikuchoson.id
    for r in rows:
        pcode = r["都道府県コード"]
        if pcode not in pref_ids:
            pref_ids[pcode] = conn.execute(
                "INSERT INTO prefectures (jis_code, name) VALUES (%s, %s) RETURNING id",
                (pcode, r["都道府県名"]),
            ).fetchone()["id"]
        mcode = r["市区町村コード"]
        if mcode not in muni_ids:
            muni_ids[mcode] = conn.execute(
                """INSERT INTO shikuchoson (prefecture_id, jis_code, name)
                   VALUES (%s, %s, %s) RETURNING id""",
                (pref_ids[pcode], mcode, r["市区町村名"]),
            ).fetchone()["id"]
        conn.execute(
            """INSERT INTO chibankuiki (shikuchoson_id, choaza_code, name, lat, lng)
               VALUES (%s, %s, %s, %s, %s)""",
            (
                muni_ids[mcode],
                # 大字町丁目コード12桁の下6桁（大字下3桁+丁目3桁）。国土地理協会コード互換
                r["大字町丁目コード"][-6:],
                r["大字町丁目名"],
                float(r["緯度"]), float(r["経度"]),
            ),
        )
    print(
        f"住所マスタを投入しました: {len(pref_ids)} 都道府県・{len(muni_ids)} 市区町村・"
        f"{len(rows)} 地番区域（{os.path.basename(ADDRESS_CSV_PATH)}）"
    )
    return True


def seed_parcels_if_empty(conn):
    n = conn.execute("SELECT count(*) AS n FROM parcels").fetchone()["n"]
    if n > 0:
        return False
    parcels, source = _load_seed_parcels()
    town_ids = {
        r["name"]: r["id"]
        for r in conn.execute("SELECT id, name FROM chibankuiki").fetchall()
    }
    # 5万筆規模になるため COPY で一括投入する（id は IDENTITY が自動採番。
    # 単一 COPY ストリームなので採番順 = シードの出現順になり、ORDER BY id で安定する）
    with conn.cursor() as cur:
        with cur.copy(
            "COPY parcels (chibankuiki_id, chiban, geometry) FROM STDIN"
        ) as copy:
            for p in parcels:
                name = _seed_town_name(p["oaza"], p["chome"])
                town_id = town_ids.get(name)
                if town_id is None:
                    raise RuntimeError(
                        f"地番区域マスタに存在しない町名です: {name}。"
                        f"{os.path.basename(ADDRESS_CSV_PATH)} を更新してください"
                    )
                copy.write_row((
                    town_id, p["chiban"],
                    json.dumps(p["geometry"], separators=(",", ":")),
                ))
    print(f"筆マスタを投入しました: {len(parcels):,} 筆（{source}）")
    return True


