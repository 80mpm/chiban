def _parse_share(share):
    """持分文字列 '1520/6755' → (分子, 分母)。分数として解釈できなければ (None, None)。"""
    s = (share or "").strip()
    if "/" not in s:
        return None, None
    num, _, den = s.partition("/")
    try:
        num, den = int(num.strip()), int(den.strip())
    except ValueError:
        return None, None
    return (num, den) if den > 0 else (None, None)


def _replace_owners(conn, land_id, owners):
    """土地の地権者を全置換する（DELETE 旧 + INSERT 新）。name 空は捨てる。"""
    conn.execute("DELETE FROM land_owners WHERE land_id = %s", (land_id,))
    for o in owners or []:
        name = (o.get("name") or "").strip()
        if not name:
            continue
        num, den = _parse_share(o.get("share"))
        conn.execute(
            "INSERT INTO land_owners (land_id, name, share_num, share_den) VALUES (%s, %s, %s, %s)",
            (land_id, name, num, den),
        )


def _fetch_parcel(conn, parcel_id):
    row = conn.execute(
        "SELECT * FROM parcels WHERE id = %s", (_parse_parcel_id(parcel_id),)
    ).fetchone()
    if not row:
        raise ApiError(400, "筆マスタに存在しない筆です")
    return row


def _fetch_land_joined(conn, project_id, land_id):
    row = conn.execute(
        _LAND_SELECT + " WHERE l.project_id = %s AND l.id = %s", (project_id, land_id)
    ).fetchone()
    if not row:
        raise ApiError(404, "土地が見つかりません")
    return row


def create_land(conn, project_id, fields):
    pid = _parse_project_id(project_id)
    if not conn.execute("SELECT 1 FROM projects WHERE id = %s", (pid,)).fetchone():
        raise ApiError(404, "案件が見つかりません")
    parcel = _fetch_parcel(conn, fields.get("parcelId"))
    status = fields.get("status") or "target"
    if status not in ("target", "acquired"):
        raise ApiError(400, "不正なステータスです")
    area = fields.get("areaTsubo")
    if area is None:
        area = polygon_area_tsubo(parcel_ring(parcel["geometry"]))
    land_id = _uuid()
    try:
        conn.execute(
            """INSERT INTO lands (id, project_id, parcel_id, description, area_tsubo, status)
               VALUES (%s, %s, %s, %s, %s, %s)""",
            (
                land_id, pid, parcel["id"],
                fields.get("description") or "", area, status,
            ),
        )
    except psycopg.errors.UniqueViolation:
        raise ApiError(409, "この筆はすでにこの案件に追加済みです")
    _replace_owners(conn, land_id, fields.get("owners"))
    row = conn.execute(
        _LAND_SELECT + " WHERE l.project_id = %s AND l.parcel_id = %s",
        (pid, parcel["id"]),
    ).fetchone()
    conn.execute("UPDATE projects SET updated_at = now() WHERE id = %s", (pid,))
    return _land_json(row, visits=[])


def update_land(conn, project_id, land_id, fields):
    pid = _parse_project_id(project_id)
    land = _fetch_land_joined(conn, pid, land_id)

    sets, params = [], []
    # 筆の付け替え: マスタに存在する parcelId のみ受け付け、坪数を再導出する
    if fields.get("parcelId") is not None and _parse_parcel_id(fields["parcelId"]) != land["parcel_id"]:
        parcel = _fetch_parcel(conn, fields["parcelId"])
        sets += ["parcel_id = %s", "area_tsubo = %s"]
        params += [parcel["id"], polygon_area_tsubo(parcel_ring(parcel["geometry"]))]
    if "owners" in fields:
        _replace_owners(conn, land_id, fields["owners"] if isinstance(fields["owners"], list) else [])
    if "description" in fields:
        sets.append("description = %s")
        params.append(fields["description"] or "")
    if "areaTsubo" in fields and fields["areaTsubo"] is not None:
        sets.append("area_tsubo = %s")
        params.append(fields["areaTsubo"])
    if "status" in fields:
        if fields["status"] not in ("target", "acquired"):
            raise ApiError(400, "不正なステータスです")
        sets.append("status = %s")
        params.append(fields["status"])
    sets.append("updated_at = now()")

    try:
        conn.execute(
            f"UPDATE lands SET {', '.join(sets)} WHERE id = %s", params + [land_id]
        )
    except psycopg.errors.UniqueViolation:
        raise ApiError(409, "この筆はすでにこの案件に追加済みです")
    return _land_json(_fetch_land_joined(conn, pid, land_id))  # visits はクライアント側で保持


def delete_land(conn, project_id, land_id):
    pid = _parse_project_id(project_id)
